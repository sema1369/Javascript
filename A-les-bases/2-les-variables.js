// 2 - LES VARIABLES - SEMA. 

/* ======================================= A - DECLARATION D'UNE VARIABLE ====================================== */

let prenom = 'Sema';
console.log(prenom);
// let = Je déclare ma variable.
// prenom = Je donne un nom à ma variable.
// Le signe égal '=' est un opérateur d'affectation. 
// J'affecte à ma variable une valeur = 'Sema'.
// console.log() permet d'afficher les resultats dans la console du naviguateur.
 
let nom = 'Stg';
console.log(nom);

/* ====================================== B - REAFFECTATION D'UNE VARIABLE  ===================================== */

nom = 'Sankara';
console.log(nom);
// Je réaffecte une autre valeur à ma variable nom.