// 8 - LES OPERATEURS ARYTHMETIQUES - SEMA

let x = 10;
let y = 3;

console.log(x + y); /* Addition : le résultat est 13*/
console.log(x - y); /* Soustraction : le résultat est 7 */
console.log(x * y); /* Multiplication : le résultat est 30*/
console.log(x / y); /* Division : le résultat est 3.3333333 */
console.log(x % y); /* Modulo : le résultat est 1*/
console.log(x ** y); /* Exponentielle : le résultat est 1000 */

/* ======================================== A- INCREMENTATION ======================================= */

console.log(++x); /* On incrémente de 1 : le résultat est 11  */

console.log(x++); 
console.log(x);
/* On incrémente encore de 1 : le résultat est 12 */

/* ======================================== B- DECREMENTATION ======================================= */

console.log(--y); /* On décrémente de 1 : le résultat est 2 */