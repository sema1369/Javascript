// 10 - LES OPERATEURS DE COMPARAISON - SEMA

let x = 1; 

/* ======================================== A - OPERATEUR RELATIONEL ======================================= */

console.log(x > 0); // Supérieur 

console.log(x >= 1); // Superieur ou égal

console.log(x < 1); // Inférieur

console.log(x <= 1); // Inférieur ou égal

/* ======================================== B - OPERATEUR D'EGALITE ======================================= */

console.log(x == 1); // Egal en valeur

console.log(x === 1); // Egal en valeur et en type

console.log(x !== 1); // Inégalité en valeur et en type

